
/**
 * Creates a description of a person using some data
 * 
 * @param {string} name Name of the person
 * @param {number} age Age of the person
 * @param {string} city place where he/she lives
 */
function showAMessage(name, age, city) {
    let text = name + " is " + age + " and lives in " + city;
    return text;
}

